num1 = 30
num2 = 2.34
print(type(num1))
num1 = num1 + num2
# implicit casting
print("num1", type(num1))
print("num2", type(num2))

# explicit casting
num3 = 5.8
print(num3)
print("num3", type(num3))
num4 = int(num3)
print("num4", type(num4))

age = input("Type your age:_")
print(type(age))

age = int(age)
print(type(age))

